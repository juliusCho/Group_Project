package com.chain.sjl.chat;

public class ChatVO {
	private long	 cha_no;
	private String cha_content;
	private String cha_regdate;
	private int cha_confirm;
	private long mem_no;
	private long cha_list_no;
	
	public long getCha_no() {
		return cha_no;
	}
	public void setCha_no(long cha_no) {
		this.cha_no = cha_no;
	}
	public String getCha_content() {
		return cha_content;
	}
	public void setCha_content(String cha_content) {
		this.cha_content = cha_content;
	}
	public String getCha_regdate() {
		return cha_regdate;
	}
	public void setCha_regdate(String cha_regdate) {
		this.cha_regdate = cha_regdate;
	}
	public int getCha_confirm() {
		return cha_confirm;
	}
	public void setCha_confirm(int cha_confirm) {
		this.cha_confirm = cha_confirm;
	}
	public long getMem_no() {
		return mem_no;
	}
	public void setMem_no(long mem_no) {
		this.mem_no = mem_no;
	}
	public long getCha_list_no() {
		return cha_list_no;
	}
	public void setCha_list_no(long cha_list_no) {
		this.cha_list_no = cha_list_no;
	}
	@Override
	public String toString() {
		return "ChatVO [cha_no=" + cha_no + ", cha_content=" + cha_content + ", cha_regdate=" + cha_regdate
				+ ", cha_confirm=" + cha_confirm + ", mem_no=" + mem_no + ", cha_list_no=" + cha_list_no + "]";
	}
}
